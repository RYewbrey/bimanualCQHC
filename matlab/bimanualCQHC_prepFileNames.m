cd(rawDir)
files = dir('*.csv'); files = {files.name};
rawFileName = cell(length(participantsBangor),3);

for i=1:length(participantsBangor)
    try
        rawFileName(i,:) = files(contains(files, participantsBangor{i}));
    catch
        error(['Error on participant ' participantsBangor{i} '. Make sure all participants have exactly 3 files.'])
    end
