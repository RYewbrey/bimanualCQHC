for i=1:length(participantsUoB)
    try
        rawFileNameUoB(i,:) = files(contains(files, participantsUoB{i}));
    catch
        error(['Error on participant ' participantsUoB{i} '. Make sure all participants have exactly 3 files.'])
    end
end
rawFileNameUoB = strcat(rawDirUoB, '/', rawFileNameUoB);

rawFileName = [rawFileName; rawFileNameUoB];
